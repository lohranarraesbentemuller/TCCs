	$("#image"+id).areaSelectable({ allowMultiple: false, x: 3, y: 3 });
	//alert($("#image"+id).areaSelectable('getSelected'));
}

function deletar(id)
{
valor="flag=deletar&area="+$('#image'+id).areaSelectable('getSelected')+"&src="+$('#image'+id).attr("src");
$.ajax({ 
 type:"POST", 
 url: "https://www.cfo2019.com/VAZ/enviar_foto.php", 
 data:valor,
  success: function(data){
     $('#tr_'+id).css("display","none");     
	 //alert("aqui");
	 //alert(data);
 
	 
   },
  error: function (jqXHR, exception) {
    var msg = "";
	console.log("deu ruim");
    if (jqXHR.status === 0) {
      msg = "Not connect.\n Verify Network.";
    } else if (jqXHR.status == 404) {
     msg = "Requested page not found. [404]";
	}
    else if (jqXHR.status == 500) {
     msg = "Internal Server Error [500].";
    } else if (exception === "parsererror") {
     msg = "Requested JSON parse failed.";
    } else if (exception === "timeout") {
     msg = "Time out error.";
    } else if (exception === "abort") {
     msg = "Ajax request aborted.";
    } else {
     msg = "Uncaught Error.\n" + jqXHR.responseText;
    }
//alert(html(msg));
}, 
});		

}
